    private void refreshTextFields() {
        txtFName.setText(p.getFirstName());
        txtLName.setText(p.getLastName());
        txtAge.setText(String.valueOf(p.getAge()));

        if ("Male".equals(p.getGender())) {
            rbtnMale.setSelected(true);
        }
        if ("Female".equals(p.getGender())) {
            rbtnFemale.setSelected(true);
        }
        if ("Others".equals(p.getGender())) {
            rbtnOthers.setSelected(true);
        }

        txtPhone.setText(p.getPhone());
        txtEmail.setText(p.getEmail());
        txtWorkState.setText(p.personAddress.getWorkState());
        txtSSN.setText(String.valueOf(p.getSSN()));
        txtCompanyName.setText(p.getCompanyName());
        txtIncome.setText(String.valueOf(p.getAnnualIncome()));

        txtHomeAdd.setText(p.personAddress.getHomeAddress());
        txtCountry.setText(p.personAddress.getCountry());
        txtCity.setText(p.personAddress.getCity());
        txtState.setText(p.personAddress.getState());
        txtZip.setText(p.personAddress.getZipCode());

        txtWorkAdd.setText(p.personAddress.getWorkAdd());
        txtWorkCity.setText(p.personAddress.getWorkCity());
        txtWorkState.setText(p.personAddress.getWorkState());
        txtWorkZip.setText(p.personAddress.getWorkZipCode());

    }

