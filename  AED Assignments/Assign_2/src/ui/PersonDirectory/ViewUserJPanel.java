    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String fname = txtFName.getText();
        String lname = txtLName.getText();
        int age = Integer.parseInt(txtAge.getText());
        String phone = txtPhone.getText();
        String email = txtEmail.getText();

        Long ssn = Long.parseLong(txtSSN.getText());
        String CompanyName = txtCompanyName.getText();
        Double income = Double.parseDouble(txtIncome.getText());

        String haddline = txtHomeAdd.getText();
        String country = txtCountry.getText();
        String city = txtCity.getText();
        String state = txtState.getText();
        String zip = txtZip.getText();

        String waddline = txtWorkAdd.getText();
        String wstate = txtWorkState.getText();
        String wcity = txtWorkCity.getText();
        String wzip = txtWorkZip.getText();

        if (fname.isBlank() || lname.isBlank()) {
            JOptionPane.showMessageDialog(null, "All Fields are Mandatory.");
            return;
        }
        p.setFirstName(fname);
        p.setLastName(lname);
        p.setAge(age);
        if (rbtnMale.isSelected()) {
            p.setGender("Male");
        }
        if (rbtnFemale.isSelected()) {
            p.setGender("Female");
        }
        if (rbtnOthers.isSelected()) {
            p.setGender("Others");
        }
        p.setPhone(phone);
        p.setEmail(email);
        p.setSSN(ssn);
        p.setCompanyName(CompanyName);
        p.setAnnualIncome(income);
        //Addresses

        p.personAddress.setHomeAddress(haddline);
        p.personAddress.setCountry(country);
        p.personAddress.setState(state);
        p.personAddress.setCity(city);
        p.personAddress.setZipCode(zip);

        p.personAddress.setWorkAddress(waddline);
        p.personAddress.setWorkState(wstate);
        p.personAddress.setWorkCity(wcity);
        p.personAddress.setWorkZipCode(wzip);

        JOptionPane.showMessageDialog(null, "Details successfully Updated!", "Warning", JOptionPane.WARNING_MESSAGE);

        setViewMode();
    }//GEN-LAST:event_btnSaveActionPerformed

