    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblPeople.getModel();
        model.setRowCount(0);

        for (Person a : personDirectory.getPerson()) {
            Object[] row = new Object[7];

            row[0] = a;
            //row[1]= a.getFName();
            row[1] = a.getLastName();
            row[2] = a.personAddress.getHomeAddress();
            row[3] = a.personAddress.getCity();
            row[4] = a.personAddress.getState();
            row[5] = a.personAddress.getWorkAdd();

            model.addRow(row);
        }
    }

